// Create an object called car with properties
let car = {
    type: "Sedan",
    model: "Honda",
    color: "Blue"
};

// Use typeof to check the type of object and log it
console.log(typeof car);

// Update the type property to "Toyota"
car.type = "Toyota";
console.log(car);

// Add a new property wheels with the value 4
car.wheels = 4;
console.log(car);
